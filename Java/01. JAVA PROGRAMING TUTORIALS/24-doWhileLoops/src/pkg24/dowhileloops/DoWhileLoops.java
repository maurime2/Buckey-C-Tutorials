package pkg24.dowhileloops;

//Class DoWhileLoops
public class DoWhileLoops {

    //Main Start
    public static void main(String[] args) {
        //Declare Local Variables
        int counter = 0;
        
        do{
            System.out.println(counter);
            counter++;
        }while(counter<=10);
    }//End Main
    
}//End Class DoWhileLOops
